function soma(){
    let n1 = document.getElementById("numero_um").value
    let n2 = document.getElementById("numero_dois").value
    let soma = parseInt(n1) + parseInt(n2)

    document.getElementById("res").value = soma
}

function subtracao(){
    let n1 = document.getElementById("numero_um").value
    let n2 = document.getElementById("numero_dois").value
    let soma = parseInt(n1) - parseInt(n2)

    document.getElementById("res").value = soma
}

function multiplicacao(){
    let n1 = document.getElementById("numero_um").value
    let n2 = document.getElementById("numero_dois").value
    let soma = parseInt(n1) * parseInt(n2)

    document.getElementById("res").value = soma
}

function divisao(){
    let n1 = document.getElementById("numero_um").value
    let n2 = document.getElementById("numero_dois").value
    let soma = parseInt(n1) - parseInt(n2)

    document.getElementById("res").value = soma
}